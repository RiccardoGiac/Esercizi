"""
Create a class MathOperations with a static method add that takes two 
numbers and returns their sum, and another static method multiply that 
takes two numbers and returns their product.
"""

class MathOperations:

    @staticmethod
    def add(x:int, y:int) ->int:
        return x + y
    
    @staticmethod
    def multiply(x:int, y:int) ->int:
        return x * y

        

